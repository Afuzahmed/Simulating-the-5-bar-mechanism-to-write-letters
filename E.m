function [x,y]=E(C1,C2,r)
  n=10;
  p=n-1;
  u=-r/p;
  x1=[C1+r:u:C1];
  y1=C2+2*r+0*x1;
  m=20;
  p2=m-1;
  v=(-2*r)/p2;
  y2=[C2+2*r:v:C2];
  x2=C1+0*y2;
  x3=[C1:-u:C1+r];
  y3=C2+0*x3;
  x4=[C1+r:u:C1];
  y4=C2+0*x4;
  y5=[C2:-u:C2+r];
  x5=C1+0*y5;
  p3=5-1;
  w=r/(2*p3);
  x6=[C1:w:C1+r/2];
  y6=C2+r+0*x6;
  x=[x1,x2,x3,x4,x5,x6];
  y=[y1,y2,y3,y4,y5,y6];
  plot(x,y);
end